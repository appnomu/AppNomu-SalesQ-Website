document.addEventListener('DOMContentLoaded', function() {
    const customerRange = document.getElementById('customerRange');
    const monthlyEarnings = document.getElementById('monthlyEarnings');
    
    // Default average plan price ($50)
    const avgPlanPrice = 50;
    const commissionRate = 0.3; // 30%
    
    function updateEarnings() {
        const customers = parseInt(customerRange.value);
        const earnings = Math.round(customers * avgPlanPrice * commissionRate);
        monthlyEarnings.textContent = earnings;
    }
    
    customerRange.addEventListener('input', updateEarnings);
    updateEarnings();
});
