<!-- Custom JavaScript for smooth scroll and animation fallback -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    // Initialize AOS with fallback in case script fails to load
    if (typeof AOS !== 'undefined') {
        AOS.init({
            duration: 800,
            once: true
        });
    } else {
        // If AOS fails to load, make all AOS elements visible
        document.querySelectorAll('[data-aos]').forEach(function(el) {
            el.style.opacity = 1;
            el.style.transform = 'none';
            el.style.visibility = 'visible';
        });
    }
    
    // Back to Top button functionality
    const backToTopButton = document.getElementById('backToTop');
    
    window.addEventListener('scroll', function() {
        if (window.scrollY > 300) {
            if (backToTopButton) backToTopButton.classList.add('visible');
        } else {
            if (backToTopButton) backToTopButton.classList.remove('visible');
        }
    });
    
    // Smooth scroll function
    function scrollToTop(e) {
        e.preventDefault();
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }
    
    // Scroll to top when back-to-top button is clicked
    if (backToTopButton) {
        backToTopButton.addEventListener('click', scrollToTop);
    }
    
    // Smooth scrolling for all hash links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            if (this.getAttribute('href') !== '#') {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    window.scrollTo({
                        top: target.offsetTop - 80,
                        behavior: 'smooth'
                    });
                }
            }
        });
    });
});
</script>

